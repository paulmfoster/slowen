<h3>A note about balances/signs</h3>
<p>
As mentioned in the "History" page, it's worth noting that
in Slowen, amounts you <em>have</em> (as in cash) are positive,
and amounts you <em>owe</em> are negative. Thus, in the table
above, your checking and savings account balances are shown
as positive numbers. And amounts shown for your credit cards
and such will be negative, because you owe that balance. Keep
this in mind for all amounts shown in Slowen.
</p>

