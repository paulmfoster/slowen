The audit page allows you to select a month and year.
The system will return with all the figures you or your
tax person will need for that month/year. This is
primarily useful for businesses, where deductions are
available for a wide variety of expenses, etc. The
figures themselves are important, but the most important
figure is the very bottom figure, which should always
be 0.00 (zero). If this figure is 0, your audit is in
balance. If it is not 0, then you will need to trace
back through the year to find out why, and correct the
error. In my case, I tend to mis-characterize bank
charges, and end up in having to track them down and
fix them. Keep in mind, whatever error there is is
confined to the month/year in question.
<p>
It's also worth noting that if you're keeping your
accounts properly, any money given or lent to the
business personally without compensation will also
show up here. If this happens, check how much you've
lent or given the business and ensure it's the same
amount you see. If it is, and you have not repaid
the funds, ignore the fact that this number is not
zero. You will need to let your tax person about this
loan/gift when you turn over the figures to them.
</p>
<p>
For tax purposes, you will need an audit for every
month of the year (for business). Typically, you can
run a spreadsheet for the year and put these audit
numbers in, if you're doing your own taxes. Otherwise,
you can simply print the monthly audits out and give
them to your tax person.
</p>
